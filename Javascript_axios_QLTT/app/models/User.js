class User {
    constructor(id, account, password, name, email, position, language, description, image) {
        this.id = id;
        this.account = account;
        this.password = password;
        this.name = name;
        this.email = email;
        this.position = position;
        this.language = language;
        this.description = description;
        this.image = image;
    }
}